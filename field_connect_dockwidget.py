    def set_project_crs(self):
        """Sets the project crs when connecting to Field Desktop if available,
        or uses the one set in the QGIS Project"""
        epsg_id = safe_get(
            self.api.get(f"/{self.active_project}/project", port=3001).json(),
            "resource",
            "epsgId",
        )
        crs = QgsCoordinateReferenceSystem(epsg_id)

        if crs.isValid():
            self.project.setCrs(crs)
            self.selectImportCrs.setCrs(crs)
            self.selectExportCrs.setCrs(crs)
        else:
            project_crs = self.project.crs()
            self.selectImportCrs.setCrs(project_crs)
            self.selectExportCrs.setCrs(project_crs)
            if epsg_id is None:
                self.mB.pushMessage(
                    title=self.plugin_name,
                    text=self.tr(
                        "Missing EPSG code in project properties. Using QGIS project CRS."
                    ),
                    level=Qgis.MessageLevel.Info,
                    duration=-1,
                )
            else:
                self.mB.pushWarning(
                    self.plugin_name,
                    self.tr(
                        "Invalid EPSG code in project properties: {epsgId}. Using QGIS project CRS."
                    ).format(epsgId=epsg_id),
                )

    def normalize_export_value(self, value, setup_type, dt=None):
        """
        Normalize attribute values for CSV export.

        - ValueRelation: {"a","b"} → a;b
        - datetime strings: apply timezone conversion
        - NULL → empty string
        """

        if value == NULL or value is None:
            return ""

        # ValueRelation multiselect
        if setup_type == "ValueRelation":
            inner = value[1:-1].strip()
            if not inner:
                return ""

            parts = next(
                csv.reader([inner], delimiter=",", quotechar='"', skipinitialspace=True),
                [],
            )

            return ";".join(parts)

        if dt and isinstance(value, str) and dt.can_transform(value):
            return dt.transform(value)

        return value

    def toggle_field_info(self, user="", version="", active_project=""):
        """Show user, version and active project when connected, hide if not"""
        d = {
            self.labelFieldUser: user,
            self.labelFieldVersion: version,
            self.labelFieldProject: active_project,
        }
        for k, v in d.items():
            row = self.formLayout.getWidgetPosition(k)[0]
            w = self.formLayout.itemAt(row, QFormLayout.ItemRole.FieldRole)
            if w:
                self.formLayout.removeWidget(w.widget())
            # k.setVisible(not k.isVisible())
            policy = k.sizePolicy()
            # only hide if disconnected
            if policy.horizontalPolicy() == QSizePolicy.Policy.Preferred and not self.connected:
                k.setSizePolicy(QSizePolicy.Policy.Ignored, QSizePolicy.Policy.Ignored)
            elif self.connected:
                label = QLabel(v)
                self.formLayout.setWidget(row, QFormLayout.ItemRole.FieldRole, label)
                k.setSizePolicy(QSizePolicy.Policy.Preferred, QSizePolicy.Policy.Preferred)

    # for de-/selecting items in the category field
    def handle_category_list(self, item):
        mdl = self.selectCats.model()
        mdl.blockSignals(True)
        row = mdl.indexFromItem(item).row()
        state = item.checkState()
        item_is_checked = state == Qt.CheckState.Checked
        checked_items = self.selectCats.checkedItemsData()
        any_item_in_image_cat = any(s in self.image_categories for s in checked_items)

        if any_item_in_image_cat:
            self.import_photo_form_set_enabled(True)
        else:
            self.import_photo_form_set_enabled(False)

        if row == 0:  # first de-/select all entry
            for i in range(1, self.selectCats.count()):
                mdl.item(i).setCheckState(state)
            item.setText(
                self.labels["DESELECT_ALL"]
                if item_is_checked
                else self.labels["SELECT_ALL"]
            )
        else:
            # update the first item to reflect whether all other items are checked
            all_checked = all(
                mdl.item(i).checkState() == Qt.CheckState.Checked
                for i in range(1, self.selectCats.count())
            )
            mdl.item(0).setCheckState(
                Qt.CheckState.Checked if all_checked else Qt.CheckState.Unchecked
            )
            mdl.item(0).setText(
                self.labels["DESELECT_ALL"] if all_checked else self.labels["SELECT_ALL"]
            )
        mdl.blockSignals(False)

