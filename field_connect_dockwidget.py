    def features_from_csv(
        self,
        csv_rows,
        fields,
        geom_lookup,
        date_transformer,
        geometry_types,
    ):
        features = defaultdict(list)

        geometry_supported = geometry_types is not None

        for row_id, row in csv_rows.items():
            feat = QgsFeature(fields)

            attrs = self.normalize_row_values(
                row=row,
                fields=fields,
                date_transformer=date_transformer,
            )
            feat.setAttributes(attrs)

            gj_feat = safe_get(geom_lookup, row_id)

            if geometry_supported and gj_feat and safe_get(gj_feat, "geometry"):

                geom_json = json.dumps(gj_feat["geometry"])
                geom = QgsJsonUtils.geometryFromGeoJson(geom_json)

                if geom and not geom.isEmpty():

                    geom_type = gj_feat["geometry"]["type"]

                    resolved_wkb = self._resolve_wkb_type(
                        geom_type,
                        geometry_types,
                    )

                    if QgsWkbTypes.isMultiType(resolved_wkb) and not geom.isMultipart():
                        geom.convertToMultiType()

                    feat.setGeometry(geom)

                    flat_type = QgsWkbTypes.flatType(resolved_wkb)
                    storage_key = QgsWkbTypes.displayString(flat_type)

                    features[storage_key].append(feat)

                    continue

            # fallback: feature has no usable geometry
            features["NoGeometry"].append(feat)

        return features

    def normalize_row_values(self, row, fields, date_transformer):
        attrs = []

        for field in fields:
            fname = field.name()
            raw = row.get(fname, "")

            value = self.normalize_value(
                raw_value=raw,
                field=field,
                date_transformer=date_transformer,
            )

            attrs.append(value)

        return attrs

    def normalize_value(self, raw_value, field, date_transformer):
        if raw_value is None:
            return ""

        raw_value = raw_value.strip()
        if not raw_value:
            return ""

        if date_transformer.can_transform(raw_value):
            return date_transformer.transform(raw_value)

        return raw_value

    def _resolve_wkb_type(self, geom_type, geometry_types):
        """
        Returns the correct QgsWkbType (Z dimension hardwired)
        based on the GeoJSON geometry type and the API configuration.
        """

        if geom_type == "NoGeometry":
            return QgsWkbTypes.Type.NoGeometry

        if geom_type.startswith("Multi"):
            base = geom_type[5:]
        else:
            base = geom_type

        multi_variant = f"Multi{base}"

        # no geometry support
        if geometry_types is None:
            return QgsWkbTypes.Type.NoGeometry

        # explicit schema
        if geometry_types:
            target = multi_variant if multi_variant in geometry_types else base
        else:
            # default to Multi
            target = multi_variant

        enum_name = f"{target}Z"

        return getattr(QgsWkbTypes, enum_name)

    def _check_connection_and_project(self) -> bool:
        response = self.api.get("/info")

        # if exception occurred, decorator already handled it
        # and execution would not reach here

        data = response.json()
        active_project_from_server = safe_get(data, "activeProject", default=False)

        if self.active_project != active_project_from_server:
            self.set_connection_enabled(False)
            self.field_disconnect()
            self.mB.pushCritical(self.plugin_name, self.labels["ACTIVE_PROJECT_CHANGED"])
            return False

        return True

